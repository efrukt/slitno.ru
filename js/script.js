    // Активное меню при прокрутке
    const sections = document.querySelectorAll('section[id]');
    const navLinks = document.querySelectorAll('nav a');
    
    function highlightActiveMenuItem() {
        const scrollPosition = window.scrollY;
        
        // Находим текущую видимую секцию
        let currentSection = null;
        sections.forEach(section => {
            const sectionTop = section.offsetTop - 100;
            const sectionHeight = section.offsetHeight;
            
            if (scrollPosition >= sectionTop && scrollPosition < sectionTop + sectionHeight) {
                currentSection = section.getAttribute('id');
            }
        });
        
        // Удаляем класс active со всех ссылок
        navLinks.forEach(link => {
            link.classList.remove('active');
        });
        
        // Если текущая секция найдена, добавляем класс active соответствующей ссылке
        if (currentSection) {
            const activeLink = document.querySelector(`nav a[href="#${currentSection}"]`);
            if (activeLink) {
                activeLink.classList.add('active');
            }
        } else {
            // Если секция не найдена (например, в начале страницы), активируем "Главная"
            document.querySelector('nav a[href="#"]').classList.add('active');
        }
    }
    
    // Вызываем функцию при загрузке страницы и при прокрутке
    highlightActiveMenuItem();
    window.addEventListener('scroll', highlightActiveMenuItem);

    // Обработчик клика по ссылкам меню
    navLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            // Если это ссылка на секцию на той же странице
            if (this.getAttribute('href').startsWith('#')) {
                e.preventDefault();
                
                // Удаляем класс active со всех ссылок
                navLinks.forEach(l => l.classList.remove('active'));
                
                // Добавляем класс active текущей ссылке
                this.classList.add('active');
                
                // Если ссылка не на #, прокручиваем к соответствующей секции
                const targetId = this.getAttribute('href');
                if (targetId !== '#') {
                    const targetSection = document.querySelector(targetId);
                    if (targetSection) {
                        window.scrollTo({
                            top: targetSection.offsetTop - 80,
                            behavior: 'smooth'
                        });
                    }
                } else {
                    // Если ссылка на #, прокручиваем в начало страницы
                    window.scrollTo({
                        top: 0,
                        behavior: 'smooth'
                    });
                }
            }
        });
    });

    // Функция загрузки словаря
    function loadDictionary(data) {
        const dictionaryList = document.getElementById('dictionaryList');
        const searchInput = document.getElementById('wordSearch').value.toLowerCase().trim();
        
        dictionaryList.innerHTML = '';
        
        if (data.length === 0) {
            dictionaryList.innerHTML = '<p class="no-results">Ничего не найдено</p>';
            return;
        }
        
        data.forEach(item => {
            // Создаем HTML для элемента словаря
            const itemElement = document.createElement('div');
            itemElement.className = 'dictionary-item';
            
            // Если есть поисковый запрос, выделяем найденную часть слова
            let wordDisplay = item.word;
            if (searchInput && item.word.toLowerCase().includes(searchInput)) {
                const regex = new RegExp(`(${searchInput})`, 'gi');
                wordDisplay = item.word.replace(regex, '<span class="search-highlight">$1</span>');
            }
            
            itemElement.innerHTML = `
                <h3>${wordDisplay}</h3>
                <p>${item.explanation}</p>
                <span class="tag ${item.type}">${getTagName(item.type)}</span>
            `;
            
            dictionaryList.appendChild(itemElement);
        });
    }
    
